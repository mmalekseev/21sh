#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;
	size_t	j;

	if (s != NULL)
	{
		i = ft_strlen(s);
		j = 0;
		while (j != i)
		{
			s[j] = '\0';
			j++;
		}
	}
}

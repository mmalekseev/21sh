#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	z;

	i = ft_strlen(s1);
	z = 0;
	while ((z < n) && (s2[z] != '\0'))
	{
		s1[i] = s2[z];
		i++;
		z++;
	}
	s1[i] = '\0';
	return (s1);
}
